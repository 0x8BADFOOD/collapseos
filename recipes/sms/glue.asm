main:
	ld	sp, 0xdff0

	ld	hl, VdpData
	ld	b, VdpDataEnd-VdpData
	ld	c, 0xbf
	otir

	xor	a
	out	(0xbf), a
	ld	a, 0x40
	out	(0xbf), a
	ld	bc, 0x4000
.loop1:
	xor	a
	out	(0xbe), a
	dec	bc
	ld	a, b
	or	c
	jr	nz, .loop1

	xor	a
	out	(0xbf), a
	ld	a, 0xc0
	out	(0xbf), a
	ld	hl, PaletteData
	ld	b, PaletteDataEnd-PaletteData
	ld	c, 0xbe
	otir

	xor	a
	out	(0xbf), a
	ld	a, 0x40
	out	(0xbf), a
	ld	hl, FontData
	ld	bc, FontDataEnd-FontData
.loop2:
	ld	a, (hl)
	out	(0xbe), a
	inc	hl
	dec	bc
	ld	a, b
	or	c
	jr	nz, .loop2

	ld	a, 0b11000000

	out	(0xbf), a
	ld	a, 0x81
	out	(0xbf), a

mainloop:
	; What we do here is simple. We go though all bits of port A controller
	; increasing B each time. As soon as we get a hit, we display that
	; letter. Pressed buttons go low.
	; This logic below is for the Genesis controller, which is modal. TH is
	; an output pin that swiches the meaning of TL and TR. When TH is high
	; (unselected), TL = Button B and TR = Button C. When TH is low
	; (selected), TL = Button A and TR = Start.
	ld	a, 0b11111101	; TH output, unselected
	out	(0x3f), a
	ld	b, 0x41		; a
	in	a, (0xdc)	; I/O port
	and	0b00100000	; Port A Button C pressed
	jr	z, updateLetter
	inc	b		; b
	in	a, (0xdc)
	and	0b00010000	; Port A Button B pressed
	jr	z, updateLetter
	inc	b		; c
	in	a, (0xdc)
	and	0b00001000	; Port A Right pressed
	jr	z, updateLetter
	inc	b		; d
	in	a, (0xdc)
	and	0b00000100	; Port A Left pressed
	jr	z, updateLetter
	inc	b		; e
	in	a, (0xdc)
	and	0b00000010	; Port A Down pressed
	jr	z, updateLetter
	inc	b		; f
	in	a, (0xdc)
	and	0b00000001	; Port A Up pressed
	jr	z, updateLetter
	ld	a, 0b11011101	; TH output, unselected
	out	(0x3f), a
	inc	b		; g
	in	a, (0xdc)
	and	0b00010000	; Port A Button A pressed
	jr	z, updateLetter
	inc	b		; h
	in	a, (0xdc)
	and	0b00100000	; Port A Start pressed
	jr	z, allLetters	; when start is pressed, print all letters
	; no button pressed on port A, continue to updateLetter

; Prints letter in B
updateLetter:
	xor	a
	out	(0xbf), a
	ld	a, 0x78
	out	(0xbf), a

	ld	a, b
	out	(0xbe), a
	jp	mainloop


; print all letters (reverse order)
allLetters:
	xor	a
	out	(0xbf), a
	ld	a, 0x78
	out	(0xbf), a

	ld	b, 0x5e
.loop:
	ld	a, b
	out	(0xbe), a
	xor	a
	out	(0xbe), a
	djnz	.loop
	jp	mainloop

